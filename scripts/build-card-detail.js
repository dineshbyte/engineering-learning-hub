#!/usr/bin/env node
/* Generates docs/assets/card-detail.js — a map of lesson URL -> one-line blurb
   (each lesson's <meta name="description">), consumed by the hub card detail sheet.
   Re-run after adding/editing any lesson. Zero dependencies. */
'use strict';
const fs = require('fs');
const path = require('path');

const DOCS = path.join(__dirname, '..', 'docs');
const OUT = path.join(DOCS, 'assets', 'card-detail.js');

function lessons(dir) {
  const out = [];
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    if (!ent.isDirectory()) continue;
    const ldir = path.join(dir, ent.name, 'lessons');
    if (!fs.existsSync(ldir)) continue;
    for (const f of fs.readdirSync(ldir)) {
      if (f.endsWith('.html')) out.push({ url: `${ent.name}/lessons/${f}`, file: path.join(ldir, f) });
    }
  }
  return out.sort((a, b) => (a.url < b.url ? -1 : 1));
}

function metaDescription(html) {
  // Assumes well-formed self-closing <meta> with no literal > inside the content (true for this authored corpus).
  const tag = html.match(/<meta[^>]*name=["']description["'][^>]*>/i);
  if (!tag) return '';
  const c = tag[0].match(/content=(["'])([\s\S]*?)\1/i);
  return c ? c[2].trim() : '';
}

function decodeEntities(s) {
  return s
    .replace(/&#x([0-9a-fA-F]+);/g, function (_, h) { return String.fromCodePoint(parseInt(h, 16)); })
    .replace(/&#(\d+);/g, function (_, d) { return String.fromCodePoint(parseInt(d, 10)); })
    .replace(/&nbsp;/g, ' ').replace(/&ndash;/g, '–').replace(/&mdash;/g, '—')
    .replace(/&lsquo;/g, '‘').replace(/&rsquo;/g, '’')
    .replace(/&ldquo;/g, '“').replace(/&rdquo;/g, '”').replace(/&hellip;/g, '…')
    .replace(/&quot;/g, '"').replace(/&#39;|&apos;/g, "'")
    .replace(/&lt;/g, '<').replace(/&gt;/g, '>')
    .replace(/&amp;/g, '&');  // amp LAST so it doesn't double-decode
}

const map = {};
for (const { url, file } of lessons(DOCS)) {
  let blurb = decodeEntities(metaDescription(fs.readFileSync(file, 'utf8')));
  if (blurb.length > 200) blurb = blurb.slice(0, 197).trimEnd() + '…';
  map[url] = blurb;
}

const banner = '/* GENERATED by scripts/build-card-detail.js — do not edit; re-run the script. */\n';
fs.writeFileSync(OUT, banner + 'window.CARD_DETAIL=' + JSON.stringify(map) + ';\n');
console.log(`card-detail.js: ${Object.keys(map).length} lessons -> ${path.relative(process.cwd(), OUT)}`);
