#!/usr/bin/env node
/**
 * Hub DOM-hook guard for the Engineering Vault site (the Astro migration safety net).
 *
 * The hub page (index.html) is no longer hand-written — it's rendered server-side
 * from a tracks data array by an Astro component. analytics.js and the ARIA/keyboard
 * layer both bind to that markup by class, id, and attribute (e.g. `article.tile`,
 * `a.start[data-track]`, `button.fchip[data-cat]`, `input#q[role=combobox]`,
 * `dialog#cardsheet`). There is NO CI guard that those hooks survive a refactor of
 * the component, so a renamed class or a dropped attribute would compile cleanly,
 * pass the build, and ship — silently severing every GA4 event and breaking the
 * search combobox / skip-link / theme toggle without any visible error.
 *
 * This script reads a BUILT hub HTML file and asserts that every selector/attribute
 * the runtime depends on is present, failing the build (exit 1) instead of letting
 * a missing hook corrupt analytics in production.
 *
 * GAP (documented on purpose): this is a STATIC check of the rendered HTML. The
 * search result rows (`a.ritem#roptN[role=option]`) and the no-results catalog
 * (`.nr` markup) are generated by client-side JS at query time, so they do NOT
 * exist in the built file and CANNOT be asserted here. We assert their *container*
 * hooks instead — `div#results[role=listbox]`, `#q-status[role=status]`, and the
 * `assets/analytics.js` include that owns those rows — which is the strongest static
 * guarantee available. Verifying the runtime rows themselves needs a headless DOM
 * check (see ANALYTICS.md), which is out of scope for this zero-dep guard.
 *
 * Zero dependencies (Node built-ins only). Exit code 1 if any hook is missing.
 *
 *   node scripts/check-hub-hooks.js [path/to/built/index.html]
 *
 * Default target: dist/index.html (overridable via argv[2]).
 */

'use strict';

const fs = require('fs');
const path = require('path');

const DEFAULT_TARGET = path.join(__dirname, '..', 'dist', 'index.html');
const target = process.argv[2] ? path.resolve(process.argv[2]) : DEFAULT_TARGET;

/**
 * Each assertion: { name, test }.
 *   - name: the human-readable hook description printed in the checklist.
 *   - test: (html) => boolean — substring/regex check against the built HTML.
 * Checks are intentionally loose on incidental formatting (attribute order,
 * whitespace, quote style) and strict on the load-bearing class/id/attribute
 * tokens that the runtime actually queries.
 */
const ASSERTIONS = [
    {
        name: 'TrackCard root — article.tile',
        test: (h) => /<article[^>]*\bclass\s*=\s*["'][^"']*\btile\b/i.test(h),
    },
    {
        name: 'TrackCard cat — data-cat on a .tile',
        test: (h) =>
            /<article[^>]*\bclass\s*=\s*["'][^"']*\btile\b[^>]*\bdata-cat\s*=/i.test(h) ||
            /<article[^>]*\bdata-cat\s*=[^>]*\bclass\s*=\s*["'][^"']*\btile\b/i.test(h),
    },
    {
        name: 'Start CTA — a.start with data-track',
        test: (h) =>
            /<a[^>]*\bclass\s*=\s*["'][^"']*\bstart\b[^>]*\bdata-track\s*=/i.test(h) ||
            /<a[^>]*\bdata-track\s*=[^>]*\bclass\s*=\s*["'][^"']*\bstart\b/i.test(h),
    },
    {
        name: 'Filter chip — button.fchip',
        test: (h) => /<button[^>]*\bclass\s*=\s*["'][^"']*\bfchip\b/i.test(h),
    },
    {
        name: 'Filter chip — data-cat attribute',
        test: (h) =>
            /<button[^>]*\bclass\s*=\s*["'][^"']*\bfchip\b[^>]*\bdata-cat\s*=/i.test(h) ||
            /<button[^>]*\bdata-cat\s*=[^>]*\bclass\s*=\s*["'][^"']*\bfchip\b/i.test(h),
    },
    {
        name: 'Search input — input id="q"',
        test: (h) => /<input[^>]*\bid\s*=\s*["']q["']/i.test(h),
    },
    {
        name: 'Search input — role="combobox"',
        test: (h) =>
            /<input[^>]*\bid\s*=\s*["']q["'][^>]*\brole\s*=\s*["']combobox["']/i.test(h) ||
            /<input[^>]*\brole\s*=\s*["']combobox["'][^>]*\bid\s*=\s*["']q["']/i.test(h),
    },
    {
        name: 'Search input — aria-controls="results"',
        test: (h) => /<input[^>]*\baria-controls\s*=\s*["']results["']/i.test(h),
    },
    {
        name: 'Results listbox — div id="results" role="listbox"',
        test: (h) =>
            /<div[^>]*\bid\s*=\s*["']results["'][^>]*\brole\s*=\s*["']listbox["']/i.test(h) ||
            /<div[^>]*\brole\s*=\s*["']listbox["'][^>]*\bid\s*=\s*["']results["']/i.test(h),
    },
    {
        name: 'Live region — id="q-status" role="status"',
        test: (h) =>
            /\bid\s*=\s*["']q-status["'][^>]*\brole\s*=\s*["']status["']/i.test(h) ||
            /\brole\s*=\s*["']status["'][^>]*\bid\s*=\s*["']q-status["']/i.test(h),
    },
    {
        name: 'GitHub button — a.ghbtn',
        test: (h) => /<a[^>]*\bclass\s*=\s*["'][^"']*\bghbtn\b/i.test(h),
    },
    {
        name: 'Social link — a.soclink with aria-label',
        test: (h) =>
            /<a[^>]*\bclass\s*=\s*["'][^"']*\bsoclink\b[^>]*\baria-label\s*=/i.test(h) ||
            /<a[^>]*\baria-label\s*=[^>]*\bclass\s*=\s*["'][^"']*\bsoclink\b/i.test(h),
    },
    {
        name: 'Theme toggle — button.themebtn with onclick="toggleTheme()"',
        test: (h) =>
            /<button[^>]*\bclass\s*=\s*["'][^"']*\bthemebtn\b[^>]*\bonclick\s*=\s*["']toggleTheme\(\)["']/i.test(
                h,
            ) ||
            /<button[^>]*\bonclick\s*=\s*["']toggleTheme\(\)["'][^>]*\bclass\s*=\s*["'][^"']*\bthemebtn\b/i.test(
                h,
            ),
    },
    {
        name: 'Hero scroll cue — a.start-here-link',
        test: (h) => /<a[^>]*\bclass\s*=\s*["'][^"']*\bstart-here-link\b/i.test(h),
    },
    {
        name: 'Card detail sheet — dialog id="cardsheet"',
        test: (h) => /<dialog[^>]*\bid\s*=\s*["']cardsheet["']/i.test(h),
    },
    {
        name: 'Grid / skip-link target — main id="grid" tabindex="-1"',
        test: (h) =>
            /<main[^>]*\bid\s*=\s*["']grid["'][^>]*\btabindex\s*=\s*["']-1["']/i.test(h) ||
            /<main[^>]*\btabindex\s*=\s*["']-1["'][^>]*\bid\s*=\s*["']grid["']/i.test(h),
    },
    {
        name: 'Skip link — a.skip-link href="#grid"',
        test: (h) =>
            /<a[^>]*\bclass\s*=\s*["'][^"']*\bskip-link\b[^>]*\bhref\s*=\s*["']#grid["']/i.test(
                h,
            ) ||
            /<a[^>]*\bhref\s*=\s*["']#grid["'][^>]*\bclass\s*=\s*["'][^"']*\bskip-link\b/i.test(h),
    },
    {
        name: 'Analytics include — assets/analytics.js',
        test: (h) => /<script[^>]*\bsrc\s*=\s*["'][^"']*assets\/analytics\.js["']/i.test(h),
    },
    {
        name: 'At least one footer/track pill — a.pill',
        test: (h) => /<a[^>]*\bclass\s*=\s*["'][^"']*\bpill\b/i.test(h),
    },
];

function main() {
    if (!fs.existsSync(target)) {
        console.error(`✗ Hub HTML not found: ${target}`);
        console.error('  Build the site first, or pass the path as argv[2].');
        process.exit(1);
    }

    const html = fs.readFileSync(target, 'utf8');

    console.log(`Checking hub DOM hooks in: ${target}\n`);

    let failed = 0;
    for (const { name, test } of ASSERTIONS) {
        let ok = false;
        try {
            ok = !!test(html);
        } catch (e) {
            ok = false;
        }
        if (!ok) failed++;
        console.log(`  ${ok ? '✓' : '✗'} ${name}`);
    }

    const total = ASSERTIONS.length;
    const passed = total - failed;
    console.log('');
    if (failed === 0) {
        console.log(`PASS — all ${total} hub hooks present.`);
        process.exit(0);
    } else {
        console.error(`FAIL — ${failed} of ${total} hub hook(s) missing (${passed} present).`);
        console.error('  A missing hook silently breaks GA4 analytics and/or the ARIA layer.');
        process.exit(1);
    }
}

main();
